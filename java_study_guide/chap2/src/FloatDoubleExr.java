
// 定义圆周率 3.14， 三个圆半径1.2，2.5，6  面积

class FloatDoubleExr {
    

    public static void main(String[] args) {
        double pi = 3.14;


        double r1 = 1.2;
        double r2 = 2.5;
        double r3 = 6;


        double area1 = pi*r1*r1;
        double area2 = pi*r2*r2;
        double area3 = pi*r3*r3;


        System.out.println(r1 + " " + area1);
        System.out.println(r2 + " " + area2);
        System.out.println(r3 + " " + area3);

    }


    


}
